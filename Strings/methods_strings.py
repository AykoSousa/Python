txt = "hello world"

print(txt.capitalize()) #Primeira letra maiuscula

print(txt.casefold()) #tudo minusculo

print(txt.center()) #retorna uma string centralizada //deve haver um parametro

print(txt.count()) #retorna o numero de vezes em que um valor ocorre em uma string

print(txt.encode()) #retorna uma string encodada

print(txt.endswith()) #retorna true se uma string termina com o valor passado

print(txt.expandtabs())

print(txt.find()) #procura strings com o determinado valor passado

print(txt.strip())

print(txt.split())

